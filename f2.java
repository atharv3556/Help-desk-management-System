    private void SubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitButtonActionPerformed
        // TODO add your handling code here:
        int number_flag=0;
        int gender_flag=0;
        int check_flag=0;
        String name=TextField1.getText();
        String mobile=TextField2.getText(); 
        int length = mobile.length(); 
        if (length != 10){
            JOptionPane.showMessageDialog(null,"Phone number must contain 10 digits. \nPlease enter valid Mobile number.");
            number_flag=1;
        }
        String email=TextField3.getText();
        String gender = "";
                // If condition to check if jRadioButton2 is selected.
                if (Button1.isSelected()) {
  
                    gender = "Male";
                    
                }
  
                else if (Button2.isSelected()) {
  
                    gender = "Female";
                    
                }
                else if (Button3.isSelected()) {
  
                    gender = "Others";
                    
                }
                else {
                JOptionPane.showMessageDialog(null,"Please select the Gender");
                gender_flag=1;
                }
        String description=DescriptionArea.getText();
        String clarify="";
        if (Clarification.isSelected()){
            clarify=Clarification.getText();
        }
        else{
            JOptionPane.showMessageDialog(null,"Details must be clarified. \nPlease tick the check box");
            check_flag=1;
        }
        LocalDate localdate = LocalDate.now();
        String date=localdate.toString();
        if (number_flag == 1 || gender_flag == 1 || check_flag ==1 ){
            JOptionPane.showMessageDialog(null,"Wrong Input Provided. \nRe-enter the details");
            if (number_flag ==1){
                TextField2.setText("");
            }
        }
        else {

        DBQueries dbqueries=new DBQueries();
        try {
            long Q_id=dbqueries.insertQueryDetails(date, name, mobile, email, gender, description);
            JOptionPane.showMessageDialog(null,"Complaint has been registered successfully. \nYour query number is: "+Q_id);
            
            FileWriter Writer = new FileWriter("Queries.txt",true);
            Writer.write(Q_id+"\n"+date+"\n"+name+"\n"+mobile+"\n"+email+"\n"+gender+"\n"+description+"\n\n");
            Writer.write(System.getProperty("line.separator"));
            Writer.close();
        } catch (HelpdeskNotExist ex) {
            Logger.getLogger(f2.class.getName()).log(Level.SEVERE, null, ex);
        }   catch (IOException ex) {
                Logger.getLogger(f2.class.getName()).log(Level.SEVERE, null, ex);
        }
        

        close();
        new f2().setVisible(true);
        }
    }//GEN-LAST:event_SubmitButtonActionPerformed

