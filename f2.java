    private void TextField2KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TextField2KeyPressed
        // TODO add your handling code here:
        String phoneNumber = TextField2.getText();
        int length = phoneNumber.length();
        char c = evt.getKeyChar();
        if (evt.getKeyChar()>='0' && evt.getKeyChar()<='9')
        {
            if(length < 10){
                TextField2.setEditable(true);
            }    else{
                TextField2.setEditable(false);
                JOptionPane.showMessageDialog(null,"Upto 10 numbers are valid.");
            }
        }else
        {
            if(evt.getExtendedKeyCode()==KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode()==KeyEvent.VK_DELETE){
                TextField2.setEditable(true);
            }else{
                TextField2.setEditable(false);
                JOptionPane.showMessageDialog(null,"Only numbers are valid.");
            }
        }
    }//GEN-LAST:event_TextField2KeyPressed

